function Korok(hash, offset, value, location){
	this.hash=hash;
	this.offset=offset;
	this.value=value;
	this.location=location;
}

Korok.countHidden=function(){
	var count=0;
	var offsets=SavegameEditor._getOffsetsByHashes(Korok.HASHES_FOUND_HIDDEN);
	for(var i=0; i<Korok.HASHES_FOUND_HIDDEN.length; i++){
		var val=tempFile.readU32(offsets[Korok.HASHES_FOUND_HIDDEN[i]]);
		if(val===1)
			count++;
		else if(val!==0)
			console.error('invalid korok hidden got value: '+val);
	}
	return count;
}
Korok.countCarry=function(){
	var count=0;
	var offsets=SavegameEditor._getOffsetsByHashes(Korok.HASHES_FOUND_CARRY);
	for(var i=0; i<Korok.HASHES_FOUND_CARRY.length; i++){
		var val=tempFile.readU32(offsets[Korok.HASHES_FOUND_CARRY[i]]);
		if(val===Korok.CARRY_STATUS_TRUE)
			count++;
		else if(val!==Korok.CARRY_STATUS_FALSE)
			console.error('invalid korok carry got value: '+val);
	}
	return count;
}


Korok.setAllHiddenAsFound=function(){
	var count=0;
	var offsets=SavegameEditor._getOffsetsByHashes(Korok.HASHES_FOUND_HIDDEN);
	for(var i=0; i<Korok.HASHES_FOUND_HIDDEN.length; i++){
		var val=tempFile.readU32(offsets[Korok.HASHES_FOUND_HIDDEN[i]]);
		if(val===0){
			tempFile.writeU32(offsets[Korok.HASHES_FOUND_HIDDEN[i]], 1);
			count++;
		}
	}

	MarcDialogs.alert(count+' koroks set as found.');
	var foundItem=SavegameEditor._findItem('key','Obj_KorokNuts');
	if(foundItem){
		foundItem.quantity+=count;
		foundItem._htmlInputQuantity.value=foundItem.quantity;
	}else{
		var newItem=new Item('key', SavegameEditor.currentItems.key.length, 'Obj_KorokNuts', count);
		newItem.removable=true;
		SavegameEditor.currentItems.key.push(newItem);
		document.getElementById('container-key').appendChild(SavegameEditor._createItemRow(newItem));
	}

	SavegameEditor.refreshKoroksCounter();
	return count;
}
Korok.setAllCarryAsFound=function(){
	var count=0;
	var offsets=SavegameEditor._getOffsetsByHashes(Korok.HASHES_FOUND_CARRY);
	for(var i=0; i<Korok.HASHES_FOUND_CARRY.length; i++){
		var val=tempFile.readU32(offsets[Korok.HASHES_FOUND_CARRY[i]]);
		if(val===Korok.CARRY_STATUS_FALSE){
			tempFile.writeU32(offsets[Korok.HASHES_FOUND_CARRY[i]], Korok.CARRY_STATUS_TRUE);
			count++;
		}
	}

	MarcDialogs.alert(count+' koroks set as carried.');
	var foundItem=SavegameEditor._findItem('key','Obj_KorokNuts');
	if(foundItem){
		foundItem.quantity+=count*2;
		foundItem._htmlInputQuantity.value=foundItem.quantity;
	}else{
		var newItem=new Item('key', SavegameEditor.currentItems.key.length, 'Obj_KorokNuts', count*2);
		newItem.removable=true;
		SavegameEditor.currentItems.key.push(newItem);
		document.getElementById('container-key').appendChild(SavegameEditor._createItemRow(newItem));
	}

	SavegameEditor.refreshKoroksCounter();
	return count;
}

